        function isScrolledIntoView(elem) {
            var docViewTop = $(window).scrollTop();
            var docViewBottom = docViewTop + $(window).height();
            var elemTop = $(elem).offset().top;
            var elemBottom = elemTop + $(elem).height();

            return ((elemBottom >= docViewTop) && (elemTop <= docViewBottom) && (elemBottom <= docViewBottom) && (elemTop >= docViewTop));
        }
        $(window).scroll(function() {
            if(isScrolledIntoView($('#graphic')))
            { $("#graphic").addClass("check") }
        });
        $(document).ready(function(){
            function carousel() {
                var step;
                var elementsToEnd = 0;
                var visibleCount = 0;

                reCalculate();
                window.addEventListener("resize", resize);
                function resize(){
                    elementsToEnd = 0;
                    $('.carouselEmployees .elements').css('left','0px');
                    reCalculate();
                    reSizeSlide();
                }
                $('.carouselEmployees .left').click(function () {
                    reCalculate();
                    moveIt("left");
                });
                $('.carouselEmployees .right').click(function () {
                    reCalculate();
                    moveIt("right");
                });

                function moveIt(side) {
                    if (side == "right") {
                        if ($('.carouselEmployees .slider').find('.element').length > visibleCount) {
                            if (elementsToEnd != visibleCount - $('.carouselEmployees .slider').find('.element').length) {
                                $('.carouselEmployees .carousel-arrow-left').css('opacity', '1');
                                $('.carouselEmployees .main').find('.elements').animate({
                                    left: "-=" + step
                                }, 100, function () { });
                                elementsToEnd--;
                                if (elementsToEnd == visibleCount - $('.carouselEmployees .slider').find('.element').length) {
                                    $('.carouselEmployees .carousel-arrow-right').css('opacity', '0.4');
                                }
                            } else{
                                $('.carouselEmployees .carousel-arrow-right').css('opacity', '0.4');
                            }
                        }
                    }
                    else {
                        if (elementsToEnd != 0) {
                            $('.carouselEmployees .carousel-arrow-right').css('opacity', '1');
                            $('.carouselEmployees .slider').find('.elements').animate({
                                left: "+=" + step
                            }, 100, function () { });
                            elementsToEnd++;
                            if (elementsToEnd == 0) {
                                $('.carouselEmployees .carousel-arrow-left').css('opacity', '0.4');
                            }
                        } else{
                            $('.carouselEmployees .carousel-arrow-left').css('opacity', '0.4');
                        }
                    }
                }
                function reCalculate() {
                    //                step = 255;
                    step = parseInt($('.element').css('width')) + parseInt($(".employee").css('padding-left')) + parseInt($(".employee").css('padding-right'));
                    visibleCount = parseInt((parseInt($('.carouselEmployees .slider').find('.main').css('width'))-80)/ step);
                    $('.carouselEmployees .visiblePart').css('width', visibleCount * parseInt(step));
                }
                function reSizeSlide() {
                    $('.carouselEmployees .main').find('.element').each(function () {
                        $(this).animate({
                            left: "0px"
                        }, 200, function () { });
                    });
                }
            }
            carousel();
        });
    </script>
